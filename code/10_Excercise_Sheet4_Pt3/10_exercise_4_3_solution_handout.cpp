#include<iostream>
using namespace std;

int main ()
{
	float a = 2;
	int b = 4;
	
	// Print Addition Result to Screen
	cout << "Addition Result (a+b) = " << a + b << endl;
	
	// Print Subtraction Result to Screen
	cout << "Subtraction Result (a-b) = " << a - b << endl;
	
	// Print Multiplication Result to Screen
	cout << "Multiplication Result (a*b) = " << a * b << endl;
	
	// Print Division Result to Screen
	cout << "Division Result (a/b) = " << a / b << endl;
	
	return 0;
}